        // **** Symbol Functionalities
        const ichangeSymbolColors = document.getElementsByClassName("ichange_symbol_clr");

        for (let i = 0; i < ichangeSymbolColors.length; i++) {
            const ichangeSymbolColor = ichangeSymbolColors[i];

            let ichangeClickCount = 0;

            const ichangeSavedClickCount = localStorage.getItem(`ichangeClickCount${i}`);
        if (ichangeSavedClickCount !== null) {
            ichangeClickCount = parseInt(ichangeSavedClickCount, 10);
        }

        const ichangeSavedBackgroundColor = localStorage.getItem(`ichangeBgColor${i}`);
        if (ichangeSavedBackgroundColor !== null) {
            ichangeSymbolColor.style.backgroundColor = ichangeSavedBackgroundColor;
        }


        let ichangePressTimer;
        ichangeSymbolColor.addEventListener("mousedown", () => {
            ichangePressTimer = window.setTimeout(() => {
            // Reset the data
            ichangeClickCount = -1;
            localStorage.removeItem(`ichangeClickCount${i}`);
            localStorage.removeItem(`ichangeBgColor${i}`);
            }, 550);
        });
        ichangeSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(ichangePressTimer);
        });


            ichangeSymbolColor.addEventListener("click", () => {
                ichangeClickCount++;
    
                if ( ichangeClickCount === 0 ){
                    ichangeSymbolColor.style.backgroundColor = "#212529";
                } else if( ichangeClickCount === 1 ){
                    ichangeSymbolColor.style.backgroundColor = "blue";
                }  else if(ichangeClickCount === 2)  {
                    ichangeSymbolColor.style.backgroundColor = "red";
                } else {
                    ichangeSymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`ichangeClickCount${i}`, ichangeClickCount);
            localStorage.setItem(`ichangeBgColor${i}`, ichangeSymbolColor.style.backgroundColor);

            });
        };

        // ** flash color start
        const ichangeLastFlashDate = localStorage.getItem('ichangeLastFlashDate');

        let ichangeAllDataSaved = true;
        const ichangeTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < ichangeSymbolColors.length; k++) {
            const ichangeSavedClick = localStorage.getItem(`ichangeClickCount${k}`);
            
            const ichangeSavedColor = localStorage.getItem(`ichangeBgColor${k}`);
            if ( !ichangeLastFlashDate || ichangeSavedClick === 0 || ichangeSavedColor === ichangeTargetColor) {   
                ichangeAllDataSaved = false;   
                break;
            }
        }

        if ((!ichangeLastFlashDate || (new Date() - new Date(ichangeLastFlashDate)) >= 24 * 60 * 60 * 1000) && !ichangeAllDataSaved) {
            const ichangeFlashSymbolColors = document.getElementsByClassName("ichange_symbol_clr");
            const ichangeColors = ["blue", "red", "green"];
            let ichangeColorIndex = 0;

            function ichangeFlashColors() {
                for (let l = 0; l < ichangeFlashSymbolColors.length; l++) {
                    const ichangeFlashSymbolColor = ichangeFlashSymbolColors[l];
                    const ichangeOriginalColor = ichangeFlashSymbolColor.style.backgroundColor;
                    ichangeFlashSymbolColor.style.backgroundColor = ichangeColors[ichangeColorIndex];
                    ichangeColorIndex = (ichangeColorIndex + 1) % ichangeColors.length;
                    setTimeout(() => {
                        ichangeFlashSymbolColor.style.backgroundColor = ichangeOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('ichangeLastFlashDate', new Date());
            ichangeFlashColors();
        }
        //**  flash color end
