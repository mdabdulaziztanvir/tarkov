        ichangeSymbolColor.addEventListener("mousedown", () => {
            ichangePressTimer = window.setTimeout(() => {
            // Reset the data
            ichangeClickCount = -1;
            localStorage.removeItem(`ichangeClickCount${i}`);
            localStorage.removeItem(`ichangeBgColor${i}`);
            }, 550);
        });
        ichangeSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(ichangePressTimer);
        });


            ichangeSymbolColor.addEventListener("click", () => {
                ichangeClickCount++;
    
                if ( ichangeClickCount === 0 ){
                    ichangeSymbolColor.style.backgroundColor = "#212529";
                } else if( ichangeClickCount === 1 ){
                    ichangeSymbolColor.style.backgroundColor = "blue";
                }  else if(ichangeClickCount === 2)  {
                    ichangeSymbolColor.style.backgroundColor = "red";
                } else {
                    ichangeSymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`ichangeClickCount${i}`, ichangeClickCount);
            localStorage.setItem(`ichangeBgColor${i}`, ichangeSymbolColor.style.backgroundColor);

            });
        };

        // ** flash color start
        const ichangeLastFlashDate = localStorage.getItem('ichangeLastFlashDate');

        let ichangeAllDataSaved = true;
        const ichangeTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < ichangeSymbolColors.length; k++) {
            const ichangeSavedClick = localStorage.getItem(`ichangeClickCount${k}`);
            
            const ichangeSavedColor = localStorage.getItem(`ichangeBgColor${k}`);
            if ( !ichangeLastFlashDate || ichangeSavedClick === 0 || ichangeSavedColor === ichangeTargetColor) {   
                ichangeAllDataSaved = false;   
                break;
            }
        }

        if ((!ichangeLastFlashDate || (new Date() - new Date(ichangeLastFlashDate)) >= 24 * 60 * 60 * 1000) && !ichangeAllDataSaved) {
