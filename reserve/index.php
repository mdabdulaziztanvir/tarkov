        // **** Symbol Functionalities
        const reserveSymbolColors = document.getElementsByClassName("reserve_symbol_color");

        for (let i = 0; i < reserveSymbolColors.length; i++) {
            const reserveSymbolColor = reserveSymbolColors[i];

            let reserveClickCount = 0;

            const reserveSavedClickCount = localStorage.getItem(`reserveClickCount${i}`);
        if (reserveSavedClickCount !== null) {
            reserveClickCount = parseInt(reserveSavedClickCount, 10);
        }

        const reserveSavedBackgroundColor = localStorage.getItem(`reserveBgColor${i}`);
        if (reserveSavedBackgroundColor !== null) {
            reserveSymbolColor.style.backgroundColor = reserveSavedBackgroundColor;
        }


        let reservePressTimer;
        reserveSymbolColor.addEventListener("mousedown", () => {
            reservePressTimer = window.setTimeout(() => {
            // Reset the data
            reserveClickCount = -1;
            localStorage.removeItem(`reserveClickCount${i}`);
            localStorage.removeItem(`reserveBgColor${i}`);
            }, 550);
        });
        reserveSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(reservePressTimer);
        });


            reserveSymbolColor.addEventListener("click", () => {
                reserveClickCount++;
    
                if ( reserveClickCount === 0 ){
                    reserveSymbolColor.style.backgroundColor = "#212529";
                } else if( reserveClickCount === 1 ){
                    reserveSymbolColor.style.backgroundColor = "blue";
                }  else if(reserveClickCount === 2)  {
                    reserveSymbolColor.style.backgroundColor = "red";
                } else {
                    reserveSymbolColor.style.backgroundColor = "green";
                }


            localStorage.setItem(`reserveClickCount${i}`, reserveClickCount);
            localStorage.setItem(`reserveBgColor${i}`, reserveSymbolColor.style.backgroundColor);

            });
        };

         // ** flash color start
         const reserveLastFlashDate = localStorage.getItem('reserveLastFlashDate');

        let reserveAllDataSaved = true;
        const reserveTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < reserveSymbolColors.length; k++) {
            const reserveSavedClick = localStorage.getItem(`reserveClickCount${k}`);
            
            const reserveSavedColor = localStorage.getItem(`reserveBgColor${k}`);
            if ( !reserveLastFlashDate || reserveSavedClick === 0 || reserveSavedColor === reserveTargetColor) {   
                reserveAllDataSaved = false;   
                break;
            }
        }

        if ((!reserveLastFlashDate || (new Date() - new Date(reserveLastFlashDate)) >= 24 * 60 * 60 * 1000) && !reserveAllDataSaved) {
            const reserveFlashSymbolColors = document.getElementsByClassName("reserve_symbol_color");
            const reserveColors = ["blue", "red", "green"];
            let reserveColorIndex = 0;

            function reserveFlashColors() {
                for (let l = 0; l < reserveFlashSymbolColors.length; l++) {
                    const reserveFlashSymbolColor = reserveFlashSymbolColors[l];
                    const reserveOriginalColor = reserveFlashSymbolColor.style.backgroundColor;
                    reserveFlashSymbolColor.style.backgroundColor = reserveColors[reserveColorIndex];
                    reserveColorIndex = (reserveColorIndex + 1) % reserveColors.length;
                    setTimeout(() => {
                        reserveFlashSymbolColor.style.backgroundColor = reserveOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('reserveLastFlashDate', new Date());
            reserveFlashColors();
        }
        //**  flash color end
