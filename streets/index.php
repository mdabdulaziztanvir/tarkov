        // **** Symbol Functionalities
        const streetSymbolColors = document.getElementsByClassName("street_symbol_color");

        for (let i = 0; i < streetSymbolColors.length; i++) {
            const streetSymbolColor = streetSymbolColors[i];
            
            let streetClickCount = 0;

            const streetSavedClickCount = localStorage.getItem(`streetClickCount${i}`);
        if (streetSavedClickCount !== null) {
            streetClickCount = parseInt(streetSavedClickCount, 10);
        }

        const streetSavedBackgroundColor = localStorage.getItem(`streetBackgroundColor${i}`);
        if (streetSavedBackgroundColor !== null) {
            streetSymbolColor.style.backgroundColor = streetSavedBackgroundColor;
        }


        let streetPressTimer;
        streetSymbolColor.addEventListener("mousedown", () => {
            streetPressTimer = window.setTimeout(() => {
            // Reset the data
            streetClickCount = -1;
            localStorage.removeItem(`streetClickCount${i}`);
            localStorage.removeItem(`streetBackgroundColor${i}`);
            }, 550);
        });
        streetSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(streetPressTimer);
        });

            streetSymbolColor.addEventListener("click", () => {
                streetClickCount++;

                if ( streetClickCount === 0 ){
                    streetSymbolColor.style.backgroundColor = "#212529";
                } else if( streetClickCount === 1 ){
                    streetSymbolColor.style.backgroundColor = "blue";
                }  else if(streetClickCount === 2)  {
                    streetSymbolColor.style.backgroundColor = "red";
                } else {
                    streetSymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`streetClickCount${i}`, streetClickCount);
            localStorage.setItem(`streetBackgroundColor${i}`, streetSymbolColor.style.backgroundColor);

            });
        };


          // ** flash color start
          const streetLastFlashDate = localStorage.getItem('streetLastFlashDate');

            let streetAllDataSaved = true;
            const streetTargetColor = "rgb(33, 37, 41)";
            for (let k = 0; k < streetSymbolColors.length; k++) {
                const streetSavedClick = localStorage.getItem(`streetClickCount${k}`);
                
                const streetSavedColor = localStorage.getItem(`streetBackgroundColor${k}`);
                if ( !streetLastFlashDate || streetSavedClick === 0 || streetSavedColor === streetTargetColor) {   
                    streetAllDataSaved = false;   
                    break;
                }
            }

            if ((!streetLastFlashDate || (new Date() - new Date(streetLastFlashDate)) >= 24 * 60 * 60 * 1000) && !streetAllDataSaved) {
                const streetFlashSymbolColors = document.getElementsByClassName("street_symbol_color");
                const streetColors = ["blue", "red", "green"];
                let streetColorIndex = 0;

                function streetFlashColors() {
                    for (let l = 0; l < streetFlashSymbolColors.length; l++) {
                        const streetFlashSymbolColor = streetFlashSymbolColors[l];
                        const streetOriginalColor = streetFlashSymbolColor.style.backgroundColor;
                        streetFlashSymbolColor.style.backgroundColor = streetColors[streetColorIndex];
                        streetColorIndex = (streetColorIndex + 1) % streetColors.length;
                        setTimeout(() => {
                            streetFlashSymbolColor.style.backgroundColor = streetOriginalColor;
                        }, 1000);
                    }
                }

                localStorage.setItem('streetLastFlashDate', new Date());
                streetFlashColors();
