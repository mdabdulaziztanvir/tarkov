        // **** Symbol Functionalities
        const lightSymbolColors = document.getElementsByClassName("light_symbol_color");

        for (let i = 0; i < lightSymbolColors.length; i++) {
            const lightSymbolColor = lightSymbolColors[i];

            let lightClickCount = 0;

            const lightSavedClickCount = localStorage.getItem(`lightClickCount${i}`);
        if (lightSavedClickCount !== null) {
            lightClickCount = parseInt(lightSavedClickCount, 10);
        }

        const lightSavedBackgroundColor = localStorage.getItem(`lightBgColor${i}`);
        if (lightSavedBackgroundColor !== null) {
            lightSymbolColor.style.backgroundColor = lightSavedBackgroundColor;
        }


        let lightPressTimer;
        lightSymbolColor.addEventListener("mousedown", () => {
            lightPressTimer = window.setTimeout(() => {
            // Reset the data
            lightClickCount = -1;
            localStorage.removeItem(`lightClickCount${i}`);
            localStorage.removeItem(`lightBgColor${i}`);
            }, 550);
        });
        lightSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(lightPressTimer);
        });


            lightSymbolColor.addEventListener("click", () => {
                lightClickCount++;
    
                if ( lightClickCount === 0 ){
                    lightSymbolColor.style.backgroundColor = "#212529";
                } else if( lightClickCount === 1 ){
                    lightSymbolColor.style.backgroundColor = "blue";
                }  else if(lightClickCount === 2)  {
                    lightSymbolColor.style.backgroundColor = "red";
                } else {
                    lightSymbolColor.style.backgroundColor = "green";
                }


            localStorage.setItem(`lightClickCount${i}`, lightClickCount);
            localStorage.setItem(`lightBgColor${i}`, lightSymbolColor.style.backgroundColor);

            });
        };

            // ** flash color start
            const lightLastFlashDate = localStorage.getItem('lightLastFlashDate');

            let lightAllDataSaved = true;
            const lightTargetColor = "rgb(33, 37, 41)";
            for (let k = 0; k < lightSymbolColors.length; k++) {
                const lightSavedClick = localStorage.getItem(`lightClickCount${k}`);
                
                const lightSavedColor = localStorage.getItem(`lightBgColor${k}`);
                if ( !lightLastFlashDate || lightSavedClick === 0 || lightSavedColor === lightTargetColor) {   
                    lightAllDataSaved = false;   
                    break;
                }
            }

            if ((!lightLastFlashDate || (new Date() - new Date(lightLastFlashDate)) >= 24 * 60 * 60 * 1000) && !lightAllDataSaved) {
                const lightFlashSymbolColors = document.getElementsByClassName("light_symbol_color");
                const lightColors = ["blue", "red", "green"];
                let lightColorIndex = 0;

                function lightFlashColors() {
                    for (let l = 0; l < lightFlashSymbolColors.length; l++) {
                        const lightFlashSymbolColor = lightFlashSymbolColors[l];
                        const lightOriginalColor = lightFlashSymbolColor.style.backgroundColor;
                        lightFlashSymbolColor.style.backgroundColor = lightColors[lightColorIndex];
                        lightColorIndex = (lightColorIndex + 1) % lightColors.length;
                        setTimeout(() => {
                            lightFlashSymbolColor.style.backgroundColor = lightOriginalColor;
                        }, 1000);
                    }
                }

                localStorage.setItem('lightLastFlashDate', new Date());
                lightFlashColors();
            }
            //**  flash color end
