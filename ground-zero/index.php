    <script>
        // **** Symbol Functionalities
        const groundZeroSymbolColors = document.getElementsByClassName("ground_zero_symbol_clr");

        for (let i = 0; i < groundZeroSymbolColors.length; i++) {
            const groundZeroSymbolColor = groundZeroSymbolColors[i];

            let groundZeroClickCount = 0;

            const groundZeroSavedClickCount = localStorage.getItem(`groundZeroClickCount${i}`);
        if (groundZeroSavedClickCount !== null) {
            groundZeroClickCount = parseInt(groundZeroSavedClickCount, 10);
        }

        const groundZeroSavedBackgroundColor = localStorage.getItem(`groundZeroColor${i}`);
        if (groundZeroSavedBackgroundColor !== null) {
            groundZeroSymbolColor.style.backgroundColor = groundZeroSavedBackgroundColor;
        }


        let groundZeroPressTimer;
        groundZeroSymbolColor.addEventListener("mousedown", () => {
            groundZeroPressTimer = window.setTimeout(() => {
            // Reset the data
            groundZeroClickCount = -1;
            localStorage.removeItem(`groundZeroClickCount${i}`);
            localStorage.removeItem(`groundZeroColor${i}`);
            }, 550);
        });
        groundZeroSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(groundZeroPressTimer);
        });


            groundZeroSymbolColor.addEventListener("click", () => {
                groundZeroClickCount++;
    
                if ( groundZeroClickCount === 0 ){
                    groundZeroSymbolColor.style.backgroundColor = "#212529";
                } else if( groundZeroClickCount === 1 ){
                    groundZeroSymbolColor.style.backgroundColor = "blue";
                }  else if(groundZeroClickCount === 2)  {
                    groundZeroSymbolColor.style.backgroundColor = "red";
                } else {
                    groundZeroSymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`groundZeroClickCount${i}`, groundZeroClickCount);
            localStorage.setItem(`groundZeroColor${i}`, groundZeroSymbolColor.style.backgroundColor);

            });
        };

        // ** flash color start
        const groundZeroLastFlashDate = localStorage.getItem('groundZeroLastFlashDate');

        let groundZeroAllDataSaved = true;
        const groundZeroTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < groundZeroSymbolColors.length; k++) {
            const groundZeroSavedClick = localStorage.getItem(`groundZeroClickCount${k}`);
            
            const groundZeroSavedColor = localStorage.getItem(`groundZeroColor${k}`);
            if ( !groundZeroLastFlashDate || groundZeroSavedClick === 0 || groundZeroSavedColor === groundZeroTargetColor) {   
                groundZeroAllDataSaved = false;   
                break;
            }
        }

        if ((!groundZeroLastFlashDate || (new Date() - new Date(groundZeroLastFlashDate)) >= 24 * 60 * 60 * 1000) && !groundZeroAllDataSaved) {
            const groundZeroFlashSymbolColors = document.getElementsByClassName("ground_zero_symbol_clr");
            const groundZeroColors = ["blue", "red", "green"];
            let groundZeroColorIndex = 0;

            function groundZeroFlashColors() {
                for (let l = 0; l < groundZeroFlashSymbolColors.length; l++) {
                    const groundZeroFlashSymbolColor = groundZeroFlashSymbolColors[l];
                    const groundZeroOriginalColor = groundZeroFlashSymbolColor.style.backgroundColor;
                    groundZeroFlashSymbolColor.style.backgroundColor = groundZeroColors[groundZeroColorIndex];
                    groundZeroColorIndex = (groundZeroColorIndex + 1) % groundZeroColors.length;
                    setTimeout(() => {
                        groundZeroFlashSymbolColor.style.backgroundColor = groundZeroOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('groundZeroLastFlashDate', new Date());
            groundZeroFlashColors();
        }
        //**  flash color end
    </script>
