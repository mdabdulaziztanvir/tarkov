        // **** Symbol Functionalities
        const customSymbolColors = document.getElementsByClassName("custom_symbol_color");

        for (let j = 0; j < customSymbolColors.length; j++) {
            const customSymbolColor = customSymbolColors[j];

            let customClick = 0;

            const customSavedClick = localStorage.getItem(`customClick${j}`);
            if (customSavedClick !== null) {
                customClick = parseInt(customSavedClick, 10);
            }

            const customSavedBg = localStorage.getItem(`customBgColor${j}`);
            if (customSavedBg !== null) {
                customSymbolColor.style.backgroundColor = customSavedBg;
            }

            let customPressTimer;
            customSymbolColor.addEventListener("mousedown", () => {
                customPressTimer = window.setTimeout(() => {
                // Reset the data
                customClick = -1;
                

                localStorage.removeItem(`customClick${j}`);
                localStorage.removeItem(`customBgColor${j}`);
                }, 550);
            });
            customSymbolColor.addEventListener("mouseup", () => {
                clearTimeout(customPressTimer);
            });
            
            
            customSymbolColor.addEventListener("click", () => {
                customClick++;
                console.log(customClick);
                if ( customClick === 0 ){
                    customSymbolColor.style.backgroundColor = "#212529";
                } else if( customClick === 1 ){
                    customSymbolColor.style.backgroundColor = "blue";
                }  else if(customClick === 2)  {
                    customSymbolColor.style.backgroundColor = "red";
                } else {
                    customSymbolColor.style.backgroundColor = "green";
                }
            
            localStorage.setItem(`customClick${j}`, customClick);
            console.log("local storge click", customClick);
            localStorage.setItem(`customBgColor${j}`, customSymbolColor.style.backgroundColor);     
            });

        };


        const lastFlashDate = localStorage.getItem('lastFlashDate');

        // Check if click and color data is saved for all elements
        let allDataSaved = true;
        const targetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < customSymbolColors.length; k++) {
            const savedClick = localStorage.getItem(`customClick${k}`);
            // console.log("sss", savedClick);
            
            const savedColor = localStorage.getItem(`customBgColor${k}`);
            if ( !lastFlashDate || savedClick === 0 || savedColor === targetColor) {
                

                allDataSaved = false;
                
                break; // Exit loop early, no need to check further
            }
        }
        


        if ((!lastFlashDate || (new Date() - new Date(lastFlashDate)) >= 24 * 60 * 60 * 1000) && !allDataSaved) {
            const flashSymbolColors = document.getElementsByClassName("custom_symbol_color");
            const colors = ["blue", "red", "green"];
            let colorIndex = 0;

            function flashColors() {
                for (let l = 0; l < flashSymbolColors.length; l++) {
                    const flashSymbolColor = flashSymbolColors[l];
                    const originalColor = flashSymbolColor.style.backgroundColor;
                    flashSymbolColor.style.backgroundColor = colors[colorIndex];
                    colorIndex = (colorIndex + 1) % colors.length;
                    setTimeout(() => {
                        flashSymbolColor.style.backgroundColor = originalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('lastFlashDate', new Date());
            flashColors();
        }

