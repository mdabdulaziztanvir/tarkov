        // **** Symbol Functionalities
        const otherQuestSymbolColors = document.getElementsByClassName("otherQuest_symbol_clr");

        for (let i = 0; i < otherQuestSymbolColors.length; i++) {
            const otherQuestSymbolColor = otherQuestSymbolColors[i];

            let otherQuestClickCount = 0;

            const otherQuestSavedClickCount = localStorage.getItem(`otherQuestClickCount${i}`);
        if (otherQuestSavedClickCount !== null) {
            otherQuestClickCount = parseInt(otherQuestSavedClickCount, 10);
        }

        const otherQuestSavedBackgroundColor = localStorage.getItem(`otherQuestBgColor${i}`);
        if (otherQuestSavedBackgroundColor !== null) {
            otherQuestSymbolColor.style.backgroundColor = otherQuestSavedBackgroundColor;
        }


        let otherQuestPressTimer;
        otherQuestSymbolColor.addEventListener("mousedown", () => {
            otherQuestPressTimer = window.setTimeout(() => {
            // Reset the data
            otherQuestClickCount = -1;
            localStorage.removeItem(`otherQuestClickCount${i}`);
            localStorage.removeItem(`otherQuestBgColor${i}`);
            }, 550);
        });
        otherQuestSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(otherQuestPressTimer);
        });


            otherQuestSymbolColor.addEventListener("click", () => {
                otherQuestClickCount++;
    
                if ( otherQuestClickCount === 0 ){
                    otherQuestSymbolColor.style.backgroundColor = "#212529";
                } else if( otherQuestClickCount === 1 ){
                    otherQuestSymbolColor.style.backgroundColor = "blue";
                }  else if(otherQuestClickCount === 2)  {
                    otherQuestSymbolColor.style.backgroundColor = "red";
                } else {
                    otherQuestSymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`otherQuestClickCount${i}`, otherQuestClickCount);
            localStorage.setItem(`otherQuestBgColor${i}`, otherQuestSymbolColor.style.backgroundColor);

            });
        };

        // ** flash color start
        const otherQuestLastFlashDate = localStorage.getItem('otherQuestLastFlashDate');

        let otherQuestAllDataSaved = true;
        const otherQuestTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < otherQuestSymbolColors.length; k++) {
            const otherQuestSavedClick = localStorage.getItem(`otherQuestClickCount${k}`);
            
            const otherQuestSavedColor = localStorage.getItem(`otherQuestBgColor${k}`);
            if ( !otherQuestLastFlashDate || otherQuestSavedClick === 0 || otherQuestSavedColor === otherQuestTargetColor) {   
                otherQuestAllDataSaved = false;   
                break;
            }
        }

        if ((!otherQuestLastFlashDate || (new Date() - new Date(otherQuestLastFlashDate)) >= 24 * 60 * 60 * 1000) && !otherQuestAllDataSaved) {
            const otherQuestFlashSymbolColors = document.getElementsByClassName("otherQuest_symbol_clr");
            const otherQuestColors = ["blue", "red", "green"];
            let otherQuestColorIndex = 0;

            function otherQuestFlashColors() {
                for (let l = 0; l < otherQuestFlashSymbolColors.length; l++) {
                    const otherQuestFlashSymbolColor = otherQuestFlashSymbolColors[l];
                    const otherQuestOriginalColor = otherQuestFlashSymbolColor.style.backgroundColor;
                    otherQuestFlashSymbolColor.style.backgroundColor = otherQuestColors[otherQuestColorIndex];
                    otherQuestColorIndex = (otherQuestColorIndex + 1) % otherQuestColors.length;
                    setTimeout(() => {
                        otherQuestFlashSymbolColor.style.backgroundColor = otherQuestOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('otherQuestLastFlashDate', new Date());
            otherQuestFlashColors();
        }
        //**  flash color end
