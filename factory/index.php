        // **** Symbol Functionalities
        const factorySymbolColors = document.getElementsByClassName("factory_symbol_color");

        for (let i = 0; i < factorySymbolColors.length; i++) {
            const factorySymbolColor = factorySymbolColors[i];

            let factoryClickCount = 0;

            const factorySavedClickCount = localStorage.getItem(`factoryClickCount${i}`);
        if (factorySavedClickCount !== null) {
            factoryClickCount = parseInt(factorySavedClickCount, 10);
        }

        const factorySavedBackgroundColor = localStorage.getItem(`factoryBgColor${i}`);
        if (factorySavedBackgroundColor !== null) {
            factorySymbolColor.style.backgroundColor = factorySavedBackgroundColor;
        }


        let factoryPressTimer;
        factorySymbolColor.addEventListener("mousedown", () => {
            factoryPressTimer = window.setTimeout(() => {
            // Reset the data
            factoryClickCount = -1;
            localStorage.removeItem(`factoryClickCount${i}`);
            localStorage.removeItem(`factoryBgColor${i}`);
            }, 550);
        });
        factorySymbolColor.addEventListener("mouseup", () => {
            clearTimeout(factoryPressTimer);
        });


            factorySymbolColor.addEventListener("click", () => {
                factoryClickCount++;
    
                if ( factoryClickCount === 0 ){
                    factorySymbolColor.style.backgroundColor = "#212529";
                } else if( factoryClickCount === 1 ){
                    factorySymbolColor.style.backgroundColor = "blue";
                }  else if(factoryClickCount === 2)  {
                    factorySymbolColor.style.backgroundColor = "red";
                } else {
                    factorySymbolColor.style.backgroundColor = "green";
                }

            localStorage.setItem(`factoryClickCount${i}`, factoryClickCount);
            localStorage.setItem(`factoryBgColor${i}`, factorySymbolColor.style.backgroundColor);
            });
        };

        // ** flash color start
        const factoryLastFlashDate = localStorage.getItem('factoryLastFlashDate');

        let factoryAllDataSaved = true;
        const factoryTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < factorySymbolColors.length; k++) {
            const facSavedClick = localStorage.getItem(`factoryClickCount${k}`);
            
            const facSavedColor = localStorage.getItem(`factoryBgColor${k}`);
            if ( !factoryLastFlashDate || facSavedClick === 0 || facSavedColor === factoryTargetColor) {   
                factoryAllDataSaved = false;   
                break;
            }
        }

        if ((!factoryLastFlashDate || (new Date() - new Date(factoryLastFlashDate)) >= 24 * 60 * 60 * 1000) && !factoryAllDataSaved) {
            const facFlashSymbolColors = document.getElementsByClassName("factory_symbol_color");
            const facColors = ["blue", "red", "green"];
            let facColorIndex = 0;

            function facFlashColors() {
                for (let l = 0; l < facFlashSymbolColors.length; l++) {
                    const facFlashSymbolColor = facFlashSymbolColors[l];
                    const facOriginalColor = facFlashSymbolColor.style.backgroundColor;
                    facFlashSymbolColor.style.backgroundColor = facColors[facColorIndex];
                    facColorIndex = (facColorIndex + 1) % facColors.length;
                    setTimeout(() => {
                        facFlashSymbolColor.style.backgroundColor = facOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('factoryLastFlashDate', new Date());
            facFlashColors();
        }
        //**  flash color end

