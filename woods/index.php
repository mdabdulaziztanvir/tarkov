    <script>
        // **** Symbol Functionalities
        const woodsSymbolColors = document.getElementsByClassName("woods_symbol_color");

        for (let i = 0; i < woodsSymbolColors.length; i++) {
            const woodsSymbolColor = woodsSymbolColors[i];

            let woodsClickCount = 0;

            const woodsSavedClickCount = localStorage.getItem(`woodsClickCount${i}`);
        if (woodsSavedClickCount !== null) {
            woodsClickCount = parseInt(woodsSavedClickCount, 10);
        }

        const woodsSavedBackgroundColor = localStorage.getItem(`woodsBackgroundColor${i}`);
        if (woodsSavedBackgroundColor !== null) {
            woodsSymbolColor.style.backgroundColor = woodsSavedBackgroundColor;
        }


        let woodsPressTimer;
        woodsSymbolColor.addEventListener("mousedown", () => {
            woodsPressTimer = window.setTimeout(() => {
            // Reset the data
            woodsClickCount = -1;
            localStorage.removeItem(`woodsClickCount${i}`);
            localStorage.removeItem(`woodsBackgroundColor${i}`);
            }, 550);
        });
        woodsSymbolColor.addEventListener("mouseup", () => {
            clearTimeout(woodsPressTimer);
        });


            woodsSymbolColor.addEventListener("click", () => {
                woodsClickCount++;
             
                if ( woodsClickCount === 0 ){
                    woodsSymbolColor.style.backgroundColor = "#212529";
                } else if( woodsClickCount === 1 ){
                    woodsSymbolColor.style.backgroundColor = "blue";
                }  else if(woodsClickCount === 2)  {
                    woodsSymbolColor.style.backgroundColor = "red";
                } else {
                    woodsSymbolColor.style.backgroundColor = "green";
                }


            localStorage.setItem(`woodsClickCount${i}`, woodsClickCount);
            localStorage.setItem(`woodsBackgroundColor${i}`, woodsSymbolColor.style.backgroundColor);

            });
        };

        // ** flash color start
        const woodsLastFlashDate = localStorage.getItem('woodsLastFlashDate');

        let woodsAllDataSaved = true;
        const woodsTargetColor = "rgb(33, 37, 41)";
        for (let k = 0; k < woodsSymbolColors.length; k++) {
            const woodsSavedClick = localStorage.getItem(`woodsClickCount${k}`);
            
            const woodsSavedColor = localStorage.getItem(`woodsBackgroundColor${k}`);
            if ( !woodsLastFlashDate || woodsSavedClick === 0 || woodsSavedColor === woodsTargetColor) {   
                woodsAllDataSaved = false;   
                break;
            }
        }

        if ((!woodsLastFlashDate || (new Date() - new Date(woodsLastFlashDate)) >= 24 * 60 * 60 * 1000) && !woodsAllDataSaved) {
            const woodsFlashSymbolColors = document.getElementsByClassName("woods_symbol_color");
            const woodsColors = ["blue", "red", "green"];
            let woodsColorIndex = 0;

            function woodsFlashColors() {
                for (let l = 0; l < woodsFlashSymbolColors.length; l++) {
                    const woodsFlashSymbolColor = woodsFlashSymbolColors[l];
                    const woodsOriginalColor = woodsFlashSymbolColor.style.backgroundColor;
                    woodsFlashSymbolColor.style.backgroundColor = woodsColors[woodsColorIndex];
                    woodsColorIndex = (woodsColorIndex + 1) % woodsColors.length;
                    setTimeout(() => {
                        woodsFlashSymbolColor.style.backgroundColor = woodsOriginalColor;
                    }, 1000);
                }
            }

            localStorage.setItem('woodsLastFlashDate', new Date());
            woodsFlashColors();
        }
        //**  flash color end
    </script>
